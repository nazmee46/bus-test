package Model;

public class Driver {
	private int driver_id;
	private String driver_name;
	private String driver_email;
	private String driver_icno;
	private String driver_phoneno;
	private String driver_address;
	private String driver_licenseno;
	private int staff_id;
	
	public Driver() {
		
	}

	public int getDriver_id() {
		return driver_id;
	}

	public void setDriver_id(int driver_id) {
		this.driver_id = driver_id;
	}

	public String getDriver_name() {
		return driver_name;
	}

	public void setDriver_name(String driver_name) {
		this.driver_name = driver_name;
	}

	public String getDriver_email() {
		return driver_email;
	}

	public void setDriver_email(String driver_email) {
		this.driver_email = driver_email;
	}

	public String getDriver_icno() {
		return driver_icno;
	}

	public void setDriver_icno(String driver_icno) {
		this.driver_icno = driver_icno;
	}

	public String getDriver_phoneno() {
		return driver_phoneno;
	}

	public void setDriver_phoneno(String driver_phoneno) {
		this.driver_phoneno = driver_phoneno;
	}

	public String getDriver_address() {
		return driver_address;
	}

	public void setDriver_address(String driver_address) {
		this.driver_address = driver_address;
	}

	public String getDriver_licenseno() {
		return driver_licenseno;
	}

	public void setDriver_licenseno(String driver_licenseno) {
		this.driver_licenseno = driver_licenseno;
	}

	public int getStaff_id() {
		return staff_id;
	}

	public void setStaff_id(int staff_id) {
		this.staff_id = staff_id;
	}
	
}
